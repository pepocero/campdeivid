<h1>Información General</h1>
<p>En Candeivid respetamos tu derecho a controlar tus datos personales. Si deseas eliminar permanentemente tu cuenta y todos los datos asociados, esta página te explica cómo hacerlo paso a paso.</p>
<p>Ten en cuenta que la eliminación de tu cuenta es un proceso irreversible. Una vez eliminada, no podrás recuperar tu perfil, rutas guardadas, historial de compras ni ningún otro dato asociado a tu cuenta.</p>

<h1>Antes de Eliminar Tu Cuenta</h1>
<p>Antes de proceder con la eliminación, te recomendamos considerar las siguientes alternativas:</p>
<ul>
<li><strong>Desactivación temporal:</strong> Puedes desactivar temporalmente tu cuenta manteniendo tus datos guardados para una posible reactivación futura.</li>
<li><strong>Cambio de configuración de privacidad:</strong> Puedes ajustar la configuración de privacidad de tu cuenta en lugar de eliminarla completamente.</li>
<li><strong>Cancelación de suscripciones:</strong> Si tu preocupación son los cobros, puedes cancelar tu suscripción premium sin eliminar la cuenta.</li>
<li><strong>Contacto con soporte:</strong> Nuestro equipo de soporte puede ayudarte a resolver problemas específicos sin necesidad de eliminar tu cuenta.</li>
</ul>

<h1>Cómo Eliminar Tu Cuenta</h1>

<h2>Método 1: Eliminación desde tu Panel de Usuario</h2>
<p>La forma más sencilla de eliminar tu cuenta es desde tu panel de usuario:</p>
<ol>
<li>Inicia sesión en tu cuenta de Candeivid</li>
<li>Ve a tu perfil haciendo clic en tu nombre de usuario en la esquina superior derecha</li>
<li>Selecciona "Configuración de cuenta" o "Editar perfil"</li>
<li>Busca la sección "Eliminar cuenta" o "Cerrar cuenta"</li>
<li>Haz clic en "Eliminar mi cuenta permanentemente"</li>
<li>Confirma tu identidad introduciendo tu contraseña actual</li>
<li>Lee y acepta los términos de eliminación</li>
<li>Haz clic en "Confirmar eliminación"</li>
</ol>

<h2>Método 2: Solicitud por Email</h2>
<p>Si no puedes acceder a tu cuenta o prefieres gestionar la eliminación por email:</p>
<ol>
<li>Envía un email desde la dirección de correo asociada a tu cuenta a: <strong>soporte@candeivid.com</strong></li>
<li>Incluye en el asunto: "Solicitud de eliminación de cuenta"</li>
<li>En el cuerpo del mensaje incluye:
   <ul>
   <li>Tu nombre completo</li>
   <li>Tu dirección de email registrada</li>
   <li>Tu nombre de usuario (si lo recuerdas)</li>
   <li>Motivo de la eliminación (opcional)</li>
   <li>Confirmación de que entiendes que es irreversible</li>
   </ul>
</li>
<li>Responderemos en un plazo máximo de 72 horas con los pasos a seguir</li>
</ol>

<h1>Qué Sucede Cuando Eliminas Tu Cuenta</h1>

<h2>Datos que se eliminan inmediatamente:</h2>
<ul>
<li>Información del perfil (nombre, foto, biografía)</li>
<li>Rutas guardadas y favoritas</li>
<li>Comentarios y valoraciones publicadas</li>
<li>Configuraciones personalizadas</li>
<li>Historial de navegación en la plataforma</li>
<li>Tokens de acceso y sesiones activas</li>
</ul>

<h2>Datos que se conservan temporalmente (30 días):</h2>
<ul>
<li>Copias de seguridad en nuestros sistemas</li>
<li>Registros de transacciones (por motivos legales y fiscales)</li>
<li>Datos necesarios para completar reembolsos pendientes</li>
</ul>

<h2>Datos que se conservan permanentemente:</h2>
<ul>
<li>Registros de transacciones fiscales (según requiere la ley)</li>
<li>Logs de seguridad anónimos</li>
<li>Estadísticas agregadas y anónimas</li>
</ul>

<h1>Tiempo de Procesamiento</h1>
<p>El proceso de eliminación de cuenta tiene los siguientes tiempos:</p>
<ul>
<li><strong>Eliminación desde panel de usuario:</strong> Inmediata</li>
<li><strong>Eliminación por email:</strong> 1-3 días laborables</li>
<li><strong>Eliminación completa de backups:</strong> 30 días</li>
<li><strong>Confirmación final:</strong> Recibirás un email de confirmación una vez completado el proceso</li>
</ul>

<h1>Qué Pasa con las Compras y Suscripciones</h1>

